// Function to check if a number is even
function isEven(number) {
    return number % 2 === 0;
}

// Example usage:
const num1 = 4;
const num2 = 7;

console.log(`${num1} is even: ${isEven(num1)}`); // Output: 4 is even: true
console.log(`${num2} is even: ${isEven(num2)}`); // Output: 7 is even: false
